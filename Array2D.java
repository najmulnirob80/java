package learn;

public class Array2D {
public static void main(String []args) {
	
	
	String[][] name= {
						{"hi","hi2","hi3"},
						{"hi5","hi6","hi7"},
						{"hi9","hi10","hi11"}
					 };
	
	
	
	for (int i=0;i<name.length;i++ ) {
		
		System.out.println("");
		
		for (int j=0;j<name.length;j++) {
			
			
			System.out.print(name[i][j]+" ");	
			
		}
	}
	
	
}
	
	
	
}
