public class AssignmentOperator {
    public static void main (String[]args){

int num=2;
int num2=5;

num+=num2; //full meaning is " num=num+num2 "
System.out.println(num);

num*=num2; //full meaning is " num=num*num2 "
System.out.println(num);

num-=num2; //full meaning is " num=num-num2 "
System.out.println(num);

num/=num2; //full meaning is " num=num/num2 "
System.out.println(num);

num%=num2; //full meaning is " num=num%num2 "
System.out.println(num);

    }
}
